    public String mostrarPantallaGuardar(){
        String ruta  = "";
        File fichero  = null;
        JFileChooser jc = new JFileChooser();
        int seleccion=jc.showSaveDialog(jPanelDiscoDuro);
        
        //si el usuario pincha en guardar
        if(seleccion == JFileChooser.APPROVE_OPTION){
            
            fichero = jc.getSelectedFile();
            ruta = fichero.getAbsolutePath();
        }
        return ruta+".pdf";
    }
